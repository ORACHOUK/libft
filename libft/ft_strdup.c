#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	char		*new;
	int			i;
	int			size;

	size = 0;
	while (src[size])
		++size;
	if (!(new = (char*)malloc((size + 1) * sizeof(char))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
